    public function store(Request $request)
    {
        $result = $request->except('submit');
        // parse string to date for client info section
        if(isset($result['Current_Bankruptcy_Date_Filed'])){
            $result['Current_Bankruptcy_Date_Filed']=date("Y-m-d",strtotime($result['Current_Bankruptcy_Date_Filed']));
        } else {
            $result['Current_Bankruptcy_Date_Filed']=NULL;
        }
        if(isset($result['Current_DV_Orders']) && $result['Current_DV_Orders']=='No'){
            $result['Current_DV_Order_Case_Num']=NULL;
        }
        if(isset($result['Current_Juvenile_Case']) && $result['Current_Juvenile_Case']=='No'){
            $result['Current_Juvenile_Case_Num']=NULL;
        }
        if(isset($result['Current_Bankruptcy_Case']) && $result['Current_Bankruptcy_Case']=='No'){
            $result['Current_Bankruptcy_Case_Num']=NULL;
            $result['Current_Bankruptcy_Date_Filed']=NULL;
        }
        // echo "<pre>";print_r($result);die;
        $DrTemporaryOrders=DrTemporaryOrders::create($result);
        
        return redirect()->route('cases.family_law_interview_tabs',$result['case_id'])->with('success', 'Temporary Order Information Submitted Successfully.');
        
    }

