            $result['Current_Bankruptcy_Case_Num']=NULL;
            $result['Current_Bankruptcy_Date_Filed']=NULL;
        }
        
        $DrTemporaryOrders  = DrTemporaryOrders::findOrFail($id);
        if($DrTemporaryOrders){
            $DrTemporaryOrders->fill($result)->save();
            return redirect()->route('drtemporaryorders.edit',$result['case_id'])->with('success', 'Temporary Orders Information Updated Successfully.');
        } else {
            return redirect()->route('drtemporaryorders.edit',$result['case_id'])->with('error', 'Something went wrong. Please try Again.');
        }
